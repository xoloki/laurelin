//! `laurelin-setup` — Groth16 trusted setup for Laurelin circuits.
//!
//! Generates proving keys and Rust VK source files for:
//!   - DepositCircuit      (7 public inputs, IC length = 8)
//!   - WithdrawCircuit     (11 public inputs, IC length = 12)
//!   - RingTransferCircuit (32 public inputs for N=2, IC length = 33)
//!
//! Usage:
//!   cargo run --release --bin laurelin-setup -- --output-dir /path/to/out
//!
//! Outputs:
//!   <dir>/deposit_pk.bin       — proving key (CanonicalSerialize)
//!   <dir>/withdraw_pk.bin
//!   <dir>/transfer_pk.bin
//!   <dir>/deposit_vk_generated.rs
//!   <dir>/withdraw_vk_generated.rs
//!   <dir>/transfer_vk_generated.rs

use std::{
    env,
    fs::{self, File},
    io::Write,
    path::{Path, PathBuf},
};

use ark_bn254::{Bn254, Fq, G1Affine, G2Affine};
use ark_ff::{BigInteger, PrimeField};
use ark_groth16::Groth16;
use ark_serialize::CanonicalSerialize;
use ark_snark::SNARK;

use laurelin_circuit::{
    deposit::DepositCircuit, transfer::RingTransferCircuit, withdraw::WithdrawCircuit,
};

// ── Serialisation helpers ──────────────────────────────────────────────────────

fn fq_be(f: &Fq) -> [u8; 32] {
    let b = f.into_bigint().to_bytes_be();
    let mut out = [0u8; 32];
    out[32 - b.len()..].copy_from_slice(&b);
    out
}

/// G1 → 64-byte X||Y big-endian.
fn g1_bytes(p: &G1Affine) -> [u8; 64] {
    let mut out = [0u8; 64];
    out[..32].copy_from_slice(&fq_be(&p.x));
    out[32..].copy_from_slice(&fq_be(&p.y));
    out
}

/// G2 → 128-byte EIP-197: x.c1||x.c0||y.c1||y.c0 big-endian.
fn g2_bytes(p: &G2Affine) -> [u8; 128] {
    let mut out = [0u8; 128];
    out[0..32].copy_from_slice(&fq_be(&p.x.c1));
    out[32..64].copy_from_slice(&fq_be(&p.x.c0));
    out[64..96].copy_from_slice(&fq_be(&p.y.c1));
    out[96..128].copy_from_slice(&fq_be(&p.y.c0));
    out
}

/// Format a byte array as a Rust array literal, 16 bytes per line.
fn bytes_literal(b: &[u8]) -> String {
    let lines: Vec<String> = b
        .chunks(16)
        .map(|chunk| {
            let hex: Vec<String> = chunk.iter().map(|x| format!("0x{x:02x}")).collect();
            format!("        {}", hex.join(","))
        })
        .collect();
    format!("[\n{},\n    ]", lines.join(",\n"))
}

/// Emit a complete `*_vk_generated.rs` source file.
fn write_vk_rs(
    path: &Path,
    static_name: &str,
    alpha: &G1Affine,
    beta: &G2Affine,
    gamma: &G2Affine,
    delta: &G2Affine,
    ic: &[G1Affine],
) -> anyhow::Result<()> {
    let mut f = File::create(path)?;

    writeln!(f, "// Auto-generated by `laurelin-setup` — do not edit.")?;
    writeln!(f)?;
    writeln!(
        f,
        "static {static_name}: VerificationKey = VerificationKey {{"
    )?;

    writeln!(f, "    alpha: {},", bytes_literal(&g1_bytes(alpha)))?;
    writeln!(f, "    beta:  {},", bytes_literal(&g2_bytes(beta)))?;
    writeln!(f, "    gamma: {},", bytes_literal(&g2_bytes(gamma)))?;
    writeln!(f, "    delta: {},", bytes_literal(&g2_bytes(delta)))?;

    writeln!(f, "    ic: &[")?;
    for pt in ic {
        writeln!(f, "        {},", bytes_literal(&g1_bytes(pt)))?;
    }
    writeln!(f, "    ],")?;
    writeln!(f, "}};")?;

    Ok(())
}

/// Run circuit_specific_setup, write pk.bin and vk_generated.rs.
fn setup_circuit<C>(
    circuit: C,
    pk_path: &Path,
    vk_path: &Path,
    static_name: &str,
) -> anyhow::Result<()>
where
    C: ark_relations::r1cs::ConstraintSynthesizer<ark_bn254::Fr> + Clone,
{
    let mut rng = rand::thread_rng();
    let (pk, vk) = Groth16::<Bn254>::circuit_specific_setup(circuit, &mut rng)
        .map_err(|e| anyhow::anyhow!("setup failed: {e:?}"))?;

    // Write proving key
    let mut pk_data = Vec::new();
    pk.serialize_uncompressed(&mut pk_data)
        .map_err(|e| anyhow::anyhow!("pk serialize: {e:?}"))?;
    fs::write(pk_path, &pk_data)?;
    eprintln!("  Wrote {} ({} bytes)", pk_path.display(), pk_data.len());

    // Emit VK source
    write_vk_rs(
        vk_path,
        static_name,
        &vk.alpha_g1,
        &vk.beta_g2,
        &vk.gamma_g2,
        &vk.delta_g2,
        &vk.gamma_abc_g1,
    )?;
    eprintln!(
        "  Wrote {} (IC length = {})",
        vk_path.display(),
        vk.gamma_abc_g1.len()
    );

    Ok(())
}

fn main() -> anyhow::Result<()> {
    let args: Vec<String> = env::args().collect();
    let out_dir = if let Some(pos) = args.iter().position(|a| a == "--output-dir") {
        PathBuf::from(args.get(pos + 1).expect("--output-dir requires a value"))
    } else {
        PathBuf::from(".")
    };

    fs::create_dir_all(&out_dir)?;
    eprintln!("Output directory: {}", out_dir.display());

    // ── Deposit ────────────────────────────────────────────────────────────────
    eprintln!("Setting up DepositCircuit…");
    setup_circuit(
        DepositCircuit {
            r: None,
            pk: None,
            delta_c1: None,
            delta_c2: None,
            amount: None,
        },
        &out_dir.join("deposit_pk.bin"),
        &out_dir.join("deposit_vk_generated.rs"),
        "DEPOSIT_VK",
    )?;

    // ── Withdraw ───────────────────────────────────────────────────────────────
    eprintln!("Setting up WithdrawCircuit…");
    setup_circuit(
        WithdrawCircuit {
            sk: None,
            r_new: None,
            old_balance: None,
            new_balance: None,
            pk: None,
            old_c1: None,
            old_c2: None,
            new_c1: None,
            new_c2: None,
            amount: None,
        },
        &out_dir.join("withdraw_pk.bin"),
        &out_dir.join("withdraw_vk_generated.rs"),
        "WITHDRAW_VK",
    )?;

    // ── RingTransfer N=2 ───────────────────────────────────────────────────────
    eprintln!("Setting up RingTransferCircuit<2>…");
    setup_circuit(
        RingTransferCircuit::<2> {
            sk: None,
            r_new: None,
            r_decoys: [None; 2],
            r_t: None,
            r_recvs: [None; 2],
            balance: None,
            amount: None,
            new_balance: None,
            sender_idx: None,
            recv_idx: None,
            sender_pks: [None; 2],
            sender_old_c1: [None; 2],
            sender_old_c2: [None; 2],
            sender_new_c1: [None; 2],
            sender_new_c2: [None; 2],
            recv_pks: [None; 2],
            recv_delta_c1: [None; 2],
            recv_delta_c2: [None; 2],
        },
        &out_dir.join("transfer_pk.bin"),
        &out_dir.join("transfer_vk_generated.rs"),
        "TRANSFER_VK",
    )?;

    eprintln!("Done.");
    Ok(())
}
